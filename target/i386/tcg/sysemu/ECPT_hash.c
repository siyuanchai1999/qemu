uint64_t crc64_be(uint64_t crc, const void* p, uint64_t len) {
  uint64_t i, t;

  const unsigned char* _p = (const unsigned char*) (p);

  for (i = 0; i < len; i++) {
    t = ((crc >> 56) ^ (*_p++)) & 0xFF;
    crc = crc64_table[t] ^ (crc << 8);
  }
  return (crc);
}


static uint64_t ecpt_crc64_be(uint64_t crc, const void* p, uint64_t len, const uint64_t * table) {
  uint64_t i, t;

  const unsigned char* _p = (const unsigned char*) (p);

  for (i = 0; i < len; i++) {
    t = ((crc >> 56) ^ (*_p++)) & 0xFF;
    crc = table[t] ^ (crc << 8);
  }
  return (crc);
}

